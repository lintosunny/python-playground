# four in-built data types in python are;

# 1. Lists
# Dynamic sized arrays
# hetrogeneous
list1 = ["A", "B", 4, 3]

# 2. Tuple
# collection of objects separated by comma
# similar to list but immutable
tuple1 = ("A", "B", 4, 3)

# 3. Set
# Unordered collection data type
# iterable, mutable, and have no duplicate elements
set1 = (["A", "B", 4, 3])

# 4. Dictionary
# ordered (since python 3.7) collection of values
# store data values like a map
# Dictionary holds key-value pair
dic1 = {
  3: "A",
  4: "B"
}

# +---------------------+--------------------+----------------+-----------------+
# | Tuples              | Lists              | Sets           | Dictionary      |
# +---------------------+--------------------+----------------+-----------------+
# | Duplicates          | Duplicates         | No duplicates  | No duplicates   |
# | Ordered             | Ordered            | Unordered      | Unordered       |
# | Mutable             | Mutable            | Immutable      | Mutable         |
# | Indexing & slicing  | Indexing & slicing | -              | -               |             
# | faster than list    |                    |                |                 |
# +---------------------+--------------------+----------------+-----------------+
